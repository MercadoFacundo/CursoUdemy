//OPERADORES DE COMPARACION

//operador igual (==)
console.log(3 == 3);
//console.log(3 == '3');

//operador no es igual (!=)
console.log(3!=3);

//operador estrictamente igual (===)

console.log(3 === 3);
//console.log(3 === '3');

// operador desigualdad estricta (!==)

console.log(3 !== 4);

//operadores  >, >=, <, <=

console.log(3 > 4);
console.log(3 >= 3);
console.log(2 < 4);
console.log(2 <= 2);