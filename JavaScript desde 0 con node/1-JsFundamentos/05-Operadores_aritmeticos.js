
/*Operadores aritmeticos
+, -, *, /, %, **
 */

console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);
console.log(2 ** 2);

//operadores de incremento (++)

var numero = 0;
//console.log(++numero); suma antes de la variable
console.log(numero++); //suma despues de la variable
console.log(numero)

//operadores de decremento (--)

var numero = 3;
//console.log(--numero); resta antes de la variable
console.log(numero--); // resta despues de la variable
console.log(numero);
